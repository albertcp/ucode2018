/**
 * An {@link Activity} which handles a broadcast of a new tag that the device just discovered.
 */
public class NFCModule extends ReactContextBaseJavaModule {

    public String last_read_tag;
    public static NFCModule instance;

    public NFCModule(ReactApplicationContext reactContext) {
        super(reactContext);
        instance = this;
        last_read_tag = null;
    }

    public void onTagRead(String tag_id){
        last_read_tag = tag_id;
        WritableMap params = Arguments.createMap();
        this.sendEvent(this.getReactApplicationContext(),"NFC_TAG_READ",params);
    }

    @ReactMethod
    public void getLastReadTag(Promise p){
        p.resolve(last_read_tag);
        return;
    }


    private void sendEvent(ReactContext reactContext,
                           String eventName,
                           @Nullable WritableMap params) {
        reactContext
                .getJSModule(DeviceEventManagerModule.RCTDeviceEventEmitter.class)
                .emit(eventName, params);
    }


    @Override
    public String getName() {
        return "NFC";
    }
}